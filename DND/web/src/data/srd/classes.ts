export function findClass(id: string): SrdClass | undefined {
  return SRD_CLASSES.find((c) => c.id === id);
}

export function classByName(name: string): SrdClass | undefined {
  const n = name.toLowerCase();
  return SRD_CLASSES.find(
    (c) => c.name.toLowerCase() === n || c.nameEn.toLowerCase() === n || n.includes(c.nameEn.toLowerCase()) || n.includes(c.name.toLowerCase()),
  );
}

export function proficiencyBonus(level: number): number {
  return Math.floor((Math.max(1, level) - 1) / 4) + 2;
}
